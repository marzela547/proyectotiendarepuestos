    private void tabla_tiposMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabla_tiposMouseClicked
        int seleccion = this.tabla_tipos.rowAtPoint(evt.getPoint());
        TiposModelo modelo = new TiposModelo();
        modelo.setTipcodigo(Integer.parseInt(String.valueOf(this.tabla_tipos.getModel().getValueAt(seleccion, 0))));
        modelo.setTipdescripcion(String.valueOf(this.tabla_tipos.getModel().getValueAt(seleccion, 1)));
        modelo.setTipestado(String.valueOf(this.tabla_tipos.getModel().getValueAt(seleccion, 2)));
        CacheTipo cache = new CacheTipo();
        cache.setDatosCompartidos(true);
        cache.setTipo(modelo);
        TipoControlador controlador = new TipoControlador();
        controlador.setOperacion(1);
        //dispose();
        Mantenimiento_tipopantalla mantenimiento = new Mantenimiento_tipopantalla();
        mantenimiento.setVisible(true);
        dispose();
    }//GEN-LAST:event_tabla_tiposMouseClicked

