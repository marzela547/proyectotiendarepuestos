    private void LlenarDatos()
    {
        this.IdTip = TipoControlador.SetdatosromCache(this.txttipo_descripcion,this.cmbtipo_estado);
                
        if(IdTip != null)
        {            
            this.btnagregar_tipo.setEnabled(false);
            this.btnmodificar_tipo.setEnabled(true);
            this.btneliminar_tipo.setEnabled(true);
            this.cmbtipo_estado.setEnabled(true);
        }
    }
    private void LimpiarInputs()
    {
        this.txttipo_descripcion.setText(null);
  
    }
