    private void btnagregar_tipoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnagregar_tipoActionPerformed

        if(!TipoControlador.Mantenimientotipo("insertar", this.IdTip,
            this.txttipo_descripcion.getText(),"ACT",this.lberrordescripcion))
    {
        this.LimpiarInputs();
        TiposPantallas pantalla = new TiposPantallas();
        pantalla.setVisible(true);
        dispose();
        }
    }//GEN-LAST:event_btnagregar_tipoActionPerformed

    private void btnmodificar_tipoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnmodificar_tipoActionPerformed
        if(!TipoControlador.Mantenimientotipo("editar", this.IdTip,
            this.txttipo_descripcion.getText(),this.cmbtipo_estado.getSelectedItem().toString(),lberrordescripcion))
    {
        this.LimpiarInputs();
        TiposPantallas pantalla = new TiposPantallas();
        pantalla.setVisible(true);
        dispose();
        }
    }//GEN-LAST:event_btnmodificar_tipoActionPerformed

    private void btneliminar_tipoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminar_tipoActionPerformed
        if(TipoControlador.Eliminartipo(this.IdTip))
        {
            this.LimpiarInputs();
            TiposPantallas pantalla = new TiposPantallas();
            pantalla.setVisible(true);
            dispose();
        }
    }//GEN-LAST:event_btneliminar_tipoActionPerformed

