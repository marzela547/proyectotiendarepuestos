    private void tabla_puestosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabla_puestosMouseClicked
        int seleccion = this.tabla_puestos.rowAtPoint(evt.getPoint()); 
        PuestoModelo puestomodelo = new PuestoModelo();
        puestomodelo.setPuecodigo(Integer.parseInt(String.valueOf(this.tabla_puestos.getModel().getValueAt(seleccion, 0)))); 
        puestomodelo.setPuedescripcion(String.valueOf(this.tabla_puestos.getModel().getValueAt(seleccion, 1)));
        puestomodelo.setPueestado(String.valueOf(this.tabla_puestos.getModel().getValueAt(seleccion, 2)));
        CachePuesto puestocache = new CachePuesto();     
        puestocache.setDatosCompartidos(true);
        puestocache.setPuesto(puestomodelo);
        PuestoControlador puestocontrolador = new PuestoControlador();
        puestocontrolador.setOperacion(1);
        dispose();
        Mantenimiento_puestopantalla mantenimientopuesto = new Mantenimiento_puestopantalla();
        mantenimientopuesto.setVisible(true);
    }//GEN-LAST:event_tabla_puestosMouseClicked

