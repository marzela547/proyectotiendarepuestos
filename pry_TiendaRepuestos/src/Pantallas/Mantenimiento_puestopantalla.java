    private void btnmodificar_puestoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnmodificar_puestoActionPerformed
        if(!PuestoControlador.Mantenimientopuesto("editar", this.IdPue,
            this.txtpuesto_descripcion.getText(),this.cmbuesto_estado.getSelectedItem().toString(),lberrordescripcion))
        {           
            this.LimpiarInputs();
            PuestosPantalla puestopantalla = new PuestosPantalla();
            puestopantalla.setVisible(true); 
            dispose();
        }  
    }//GEN-LAST:event_btnmodificar_puestoActionPerformed

    private void btnagregar_puestoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnagregar_puestoActionPerformed

        if(!PuestoControlador.Mantenimientopuesto("insertar", this.IdPue,
            this.txtpuesto_descripcion.getText(),"ACT",this.lberrordescripcion))
        {           
            this.LimpiarInputs();
            PuestosPantalla puestopantalla = new PuestosPantalla();
            puestopantalla.setVisible(true); 
            dispose();
        }  
    }//GEN-LAST:event_btnagregar_puestoActionPerformed

