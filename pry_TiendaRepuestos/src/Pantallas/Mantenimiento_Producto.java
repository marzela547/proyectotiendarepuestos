    private void LlenarDatos()
    {
        this.IdPro = ProductoControlador.SetdatosromCache(this.txt_descripcion,
                this.txt_venta, this.cmbtipo, this.cmbmarca,
                this.txt_existencia ,this.txt_cantidad ,this.cmbestado);
                
        if(IdPro != null)
        {
            this.btnagregar_producto.setEnabled(false);
            this.btnmodificar_producto.setEnabled(true);
            this.btn_eliminar_producto.setEnabled(true);
            this.cmbestado.setEnabled(true);
            
        }
    }     
    
    
    private void LimpiarInputs()
    {
        this.txt_descripcion.setText(null);
        this.txt_venta.setText(null);
        this.txt_existencia.setText(null); 
        this.txt_cantidad.setText(null); 
    }   
