    private void btnagregar_productoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnagregar_productoActionPerformed
    if(!ProductoControlador.MantenimientoProducto("insertar", 0,  this.txt_descripcion.getText(),
                this.txt_venta.getText(),ProductosConexion.IdTipo(cmbtipo.getSelectedItem().toString()),
                ProductosConexion.IdMarca(cmbmarca.getSelectedItem().toString()),this.txt_existencia.getText(),this.txt_cantidad.getText(),
                cmbestado.getSelectedItem().toString(),
                this.errdescripcion, this.errventa,this.errexistencia ,this.errminimo))
    {
        this.LimpiarInputs();
        ProductoPantalla pantalla = new ProductoPantalla();
        pantalla.setVisible(true);
        dispose();
        }
    }//GEN-LAST:event_btnagregar_productoActionPerformed

    private void btnmodificar_productoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnmodificar_productoActionPerformed

        
        if(!ProductoControlador.MantenimientoProducto("editar",IdPro, this.txt_descripcion.getText(),
                this.txt_venta.getText(),ProductosConexion.IdTipo(cmbtipo.getSelectedItem().toString()),
                ProductosConexion.IdMarca(cmbmarca.getSelectedItem().toString()),this.txt_existencia.getText(),this.txt_cantidad.getText(), 
                cmbestado.getSelectedItem().toString(),
                this.errdescripcion, this.errventa,this.errexistencia ,this.errminimo))
    {
        this.LimpiarInputs();
        ProductoPantalla pantalla = new ProductoPantalla();
        pantalla.setVisible(true);
        dispose();
        }
    }//GEN-LAST:event_btnmodificar_productoActionPerformed

    private void btn_eliminar_productoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_eliminar_productoActionPerformed


        if(ProductoControlador.Eliminarproducto(this.IdPro))
        {
            this.LimpiarInputs();
            ProductoPantalla productopantalla = new ProductoPantalla();
            productopantalla.setVisible(true);
            dispose();

        }
    }//GEN-LAST:event_btn_eliminar_productoActionPerformed

